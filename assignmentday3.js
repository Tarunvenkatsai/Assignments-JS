// let y = x++;
// console.log(x, y);
// What will be the output and why?
// Output: 6 5
// ->x++ is a postfix increment, which means the current value of x is used first, then x is incremented.
// ->So, y is assigned the original value of x, which is 5.
// ->After that, x is incremented to 6.
// ->Therefore, console.log(x, y) prints 6 5.


